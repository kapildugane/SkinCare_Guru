def score_and_filter_products(all_products, user_profile):
    category = user_profile["category"]
    skin_type = user_profile["skin_type"]
    concerns = user_profile["concern"]
    experience = user_profile["experience"]

    base_candidates = []
    relaxed_candidates = []

    for product in all_products:
        if not product_category_matches(product, category):
            continue

        compatible, exact_skin = skin_type_compatible(product.skin_type, skin_type)
        if not compatible:
            continue

        concern_matches = product_matches_concern(product, concerns)
        effect_matches = infer_effect_match(product, concerns)

        score = 0
        matched_attributes = []

        if concern_matches:
            score += 5 * len(concern_matches)
            matched_attributes.extend([f"concern:{match}" for match in concern_matches])

        if exact_skin:
            score += 3
            matched_attributes.append(f"skin_type:{skin_type}")
        else:
            score += 2
            matched_attributes.append("skin_type:All")

        if effect_matches:
            score += 2 * len(effect_matches)
            matched_attributes.extend([f"effect:{match}" for match in effect_matches])

        beginner_safe = is_beginner_friendly(product)
        if experience == "Beginner" and beginner_safe:
            score += 1
            matched_attributes.append("beginner_friendly")
        elif experience == "Beginner" and not beginner_safe:
            score -= 2
            matched_attributes.append("complex_for_beginner")

        if len((product.name or "").split()) <= 4:
            score += 1
            matched_attributes.append("simple_profile")

        if skin_type.lower() == "sensitive" and product_is_harsh(product):
            score -= 3
            matched_attributes.append("harsh_for_sensitive")

        candidate = {
            "product_id": product.product_id,
            "product_name": product.name,
            "category": product.category or "",
            "sub_category": product.sub_category or "",
            "skin_type": product.skin_type or "",
            "concerns": product.concerns or "",
            "ingredients": product.ingredients or "",
            "routine_step": product.routine_step or "",
            "time_of_use": product.time_of_use or "",
            "age_group": product.age_group or "",
            "experience_level": product.experience_level or "",
            "beginner_friendly": product.beginner_friendly or "",
            "price": product.price or "",
            "image": product.image or "",
            "app_score": score,
            "matched_attributes": matched_attributes,
        }

        relaxed_candidates.append(candidate)
        if concern_matches or not concerns:
            base_candidates.append(candidate)

    ranked = sorted(base_candidates, key=lambda item: item["app_score"], reverse=True)
    if not ranked:
        ranked = sorted(relaxed_candidates, key=lambda item: item["app_score"], reverse=True)

    return ranked


def choose_recommendation_count(user_profile):
    flow = user_profile["flow"]
    if flow == "Build My Routine":
        return 6
    if flow == "Help Me Fix a Concern":
        return 3
    requested_count = user_profile.get("requested_count")
    try:
        return max(3, min(6, int(requested_count)))
    except (TypeError, ValueError):
        return 4


def _build_natural_reason(item, user_profile):
    """Build a natural-language reason for why a product was selected."""
    attrs = item.get("matched_attributes", [])
    skin = user_profile.get("skin_type", "your").lower()
    concerns = user_profile.get("concern", [])
    experience = user_profile.get("experience", "Beginner").lower()

    parts = []
    concern_matches = [a.split(":")[1] for a in attrs if a.startswith("concern:")]
    effect_matches = [a.split(":")[1] for a in attrs if a.startswith("effect:")]

    if concern_matches:
        parts.append(f"directly targets your {' and '.join(concern_matches).lower()} concerns")
    if any(a.startswith("skin_type:") for a in attrs):
        parts.append(f"suited for {skin} skin")
    if "beginner_friendly" in attrs:
        parts.append("easy to work into any routine")
    if effect_matches and not concern_matches:
        parts.append(f"helps with {' and '.join(effect_matches).lower()}")
    if "harsh_for_sensitive" in attrs:
        parts.append("use with caution on sensitive areas")

    if not parts:
        parts.append("a strong match for your profile")

    return parts[0][0].upper() + parts[0][1:] + (", and " + ", ".join(parts[1:]) if len(parts) > 1 else "") + "."


