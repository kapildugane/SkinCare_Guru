def _fuzzy_find_product(product_name, product_map, all_products):
    """Find a product by name using progressively looser matching."""
    name = product_name.strip().lower()
    if not name:
        return None

    # 1. Exact match
    if name in product_map:
        return product_map[name]

    # 2. Normalized match (collapse extra whitespace)
    normalized = " ".join(name.split())
    if normalized in product_map:
        return product_map[normalized]

    # 3. Substring match — check if AI name is contained in a DB name or vice versa
    for db_name, product in product_map.items():
        if name in db_name or db_name in name:
            return product

    # 4. Word-overlap match — find the product with the most words in common
    name_words = set(name.split())
    best_match = None
    best_overlap = 0
    for db_name, product in product_map.items():
        db_words = set(db_name.split())
        overlap = len(name_words & db_words)
        if overlap > best_overlap and overlap >= 2:
            best_overlap = overlap
            best_match = product

    return best_match


def map_recommendations_to_products(recommendations, all_products, scored_shortlist=None):
    """Map AI recommendations to product cards. Uses fuzzy matching and
    falls back to the scored shortlist to guarantee every recommendation gets a card."""
    product_map = {product.name.lower().strip(): product for product in all_products}
    product_list = []
    seen = set()

    for recommendation in recommendations:
        product_name = (recommendation.get("product_name") or "").strip().lower()
        if not product_name or product_name in seen:
            continue
        seen.add(product_name)

        product = _fuzzy_find_product(product_name, product_map, all_products)
        if not product:
            print(f"[WARN] Could not match product: '{product_name}'")
            continue

        brand = product.name.split(" ")[0] if " " in product.name else "Skincare"
        product_list.append({
            "name": product.name,
            "brand": brand,
            "price": product.price,
            "image": product.image
        })

    # If we still have fewer cards than recommendations, fill from the scored shortlist
    if scored_shortlist and len(product_list) < len(recommendations):
        existing_names = {p["name"].lower() for p in product_list}
        for item in scored_shortlist:
            if len(product_list) >= len(recommendations):
                break
            item_name = item.get("product_name", "").lower()
            if item_name in existing_names:
                continue
            product = product_map.get(item_name)
            if not product:
                continue
            existing_names.add(item_name)
            brand = product.name.split(" ")[0] if " " in product.name else "Skincare"
            product_list.append({
                "name": product.name,
                "brand": brand,
                "price": product.price,
                "image": product.image
            })

    return product_list


