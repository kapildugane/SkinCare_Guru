def normalize_experience(user_data):
    raw = (user_data.get("experience") or "Beginner").strip().title()
    if raw == "Expert":
        return "Advanced"
    return raw


def normalize_concern_tokens(user_data):
    raw_values = []
    concerns = user_data.get("concerns")
    if isinstance(concerns, list):
        raw_values.extend(concerns)
    elif concerns:
        raw_values.append(concerns)

    for key in ["kit_type", "symptoms", "specific_area"]:
        value = user_data.get(key)
        if value:
            raw_values.append(value)

    normalized = []
    concern_aliases = {
        "anti-aging": "Aging",
        "ageing": "Aging",
        "aging": "Aging",
        "brightening": "Dullness",
        "hydration": "Dryness",
        "oiliness": "Oily",
        "pimples": "Acne",
        "breakouts": "Acne",
        "dark spots": "Pigmentation",
    }

    for value in raw_values:
        for token in str(value).replace("/", ",").split(","):
            clean = token.strip().lower()
            if not clean:
                continue
            normalized.append(concern_aliases.get(clean, clean.title()))

    return list(dict.fromkeys(normalized))


def split_csv_text(value):
    if not value:
        return []
    return [part.strip() for part in str(value).replace("/", ",").split(",") if part.strip()]


def product_category_matches(product, normalized_category):
    target = normalized_category.lower()
    haystack = " ".join(
        filter(None, [product.category, product.sub_category, product.routine_step, product.time_of_use])
    ).lower()
    keyword_map = {
        "face": ["face", "cleanser", "serum", "moisturizer", "sunscreen", "toner"],
        "body": ["body", "lotion", "cream", "wash", "butter"],
        "hair": ["hair", "scalp", "shampoo", "conditioner", "oil", "mask"],
        "lips": ["lip", "balm", "lipcare"],
    }
    if target not in keyword_map:
        return target in haystack
    return any(keyword in haystack for keyword in keyword_map[target])


def skin_type_compatible(product_skin_text, user_skin_type):
    product_skins = [skin.lower() for skin in split_csv_text(product_skin_text)]
    user_skin = user_skin_type.lower()
    if not product_skins:
        return False, False
    if "all" in product_skins:
        return True, False
    return user_skin in product_skins, user_skin in product_skins


def product_matches_concern(product, concern_tokens):
    product_concerns = [concern.lower() for concern in split_csv_text(product.concerns)]
    matches = []
    for concern in concern_tokens:
        concern_lower = concern.lower()
        if concern_lower in product_concerns:
            matches.append(concern)
    return matches


def infer_effect_match(product, concern_tokens):
    text = " ".join(
        filter(None, [product.concerns, product.ingredients, product.sub_category, product.routine_step, product.name])
    ).lower()
    effect_keywords = {
        "acne": ["salicylic", "niacinamide", "acne", "oil", "clarifying"],
        "dryness": ["hyaluronic", "ceramide", "glycerin", "hydrating", "barrier"],
        "aging": ["retinol", "peptide", "vitamin c", "firming", "aging"],
        "dullness": ["vitamin c", "bright", "glow", "radiance"],
        "pigmentation": ["tranexamic", "alpha arbutin", "vitamin c", "pigment", "spot"],
        "sensitive": ["soothing", "calming", "centella", "ceramide", "oat"],
    }
    matched = []
    for concern in concern_tokens:
        keywords = effect_keywords.get(concern.lower(), [concern.lower()])
        if any(keyword in text for keyword in keywords):
            matched.append(concern)
    return matched


def is_beginner_friendly(product):
    flags = " ".join(filter(None, [product.beginner_friendly, product.experience_level])).lower()
    return "yes" in flags or "beginner" in flags or not flags.strip()


def product_is_harsh(product):
    text = " ".join(filter(None, [product.ingredients, product.name, product.sub_category])).lower()
    harsh_keywords = ["retinol", "glycolic", "aha", "bha", "peel", "exfoliating", "strong acid"]
    return any(keyword in text for keyword in harsh_keywords)


